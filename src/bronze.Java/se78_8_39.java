package bronze.Java;

public class se78_8_39 {

	public static void main(String[] args) {
		char c = 'b';
		switch (c) {
		case 'a':
			System.out.println("A");
			break;
		case 'b':
			System.out.println("B");
		case 'c':
			System.out.println("C");
			break;
		default:
			System.out.println("D");
			break;
		}

	}

}
